{
	return usFunc_PositTableU32(buff, len, res) + zero_index_temp;
}

 
/*****************************************************************************************************************
-----КЏЪ§ЙІФм    ЬјЙ§ADШЅМЦЫуЮТЖШ
-----ЫЕУї(БИзЂ)  Rt=R*EXP(B*(1/T1-1/T2))
-----ДЋШыВЮЪ§    none
-----ЪфГіВЮЪ§    none
-----ЗЕЛижЕ      none
******************************************************************************************************************/
double fNtc_CulcTempByAD(ntc_val_t *val,u16 adc_val)
{
    double v_x;    /* Measured voltage value */
    double rt_x;   /* Measure the resistance */
    double temp_x; /* Measure the temperature */

	v_x = (double)adc_val / val->hex_x * val->sys_vol;
    rt_x = (val->volt_res * v_x) / (val->sys_vol - v_x);
	
//    v_x = val->sys_vol - ((double)adc_val / val->hex_x * val->sys_vol);
//	rt_x = (val->volt_res * v_x) / (val->sys_vol - v_x);
	
//    NTCзїЯТР­ЪЙгУЪБЪЙгУДЫЃЌЭЌЪБНЈвщNTCзїЯТР­ЪЙгУЁЂПЩгааЇБмУтИЩШХ###
    temp_x = ( 1 / (log(rt_x/val->ntc_res) / val->b_x + (1 / 298.15) ) ) - 273.15;
	
//    NTCзїЩЯР­ЪЙгУЪБЪЙгУДЫ
//    temp_x = ( 1 / (log(rt_x / val->ntc_res) / val->b_x + (1 / 298.15) ) ) - 273.15;
    return temp_x;
}

