/***********************************************************************************************************************
-----函数功能	初始化状态显示
-----备注		100mS隔间时间运行
-----作者       LJD
-----日期       2026-01-10
************************************************************************************************************************/
void Display_ShowInitState(void)
{
	static u8 j=1;
	static u8 ALL_ON_flag=0;
	
	dispLIGHT_POWER_ON();//开灯
	
 
	if(ALL_ON_flag==0)
	{
		ALL_ON_flag=1;
		Display_SetRunMode();
		FillLcdAll1(0xFF);
	    FillLcdAll2(0xFF);
		Display_RefreshData();
		
		#if(boardUSE_OS)
		vTaskDelay(400);
		#endif  //boardUSE_OS
	}
	
	Display_ClearData();
	
 	Display_InPwr(0);     //Input界面   
    Display_OutPwr(3600);   //Out界面
	
	Display_TimRoll(1);//TIM时间滚动
	
	DisplayNum2(0,10);//TIM --
	DisplayNum2(1,10);//TIM --
	Y_Display(11);//M
	
	Display_BAT(0,false,0);         //BAT等级界面	
    T_Display(j);
	
	DisplayNum1(10,10);          //SOC界面--
	DisplayNum1(11,10);	         //SOC界面--
	

    j++;if(j>10)j=1;
	Display_RefreshData();
}

/***********************************************************************************************************************
-----函数功能	显示时间
-----传入参数   sw:0不使用时间框，1-8格数选择，9常亮
-----传入参数   min:0-5940
-----作者       LJD
-----日期       2026-01-10
************************************************************************************************************************/
void Display_Time(u8 sw,u16 min)//Tim界面
{
	u8 i=0;
	float f_min=0.00;
	
	if(min>=TIM_MAX)//>99H
	{
		DisplayNum2(0,9);
		DisplayNum2(1,9);
		Y_Display(10);//+
		Y_Display(12);//H
	}
	else
	{
		if(min>59)
		{
		   
		   Y_Display(12);//H
			
		   f_min=min/6.0;
		   min=(u16)f_min;
		   if(min<=99)
		   {
			 Y_Display(13);//.
		   }
		}
		else
		{
		   Y_Display(11);
		}	
		
		if(min>9)
		{
			
			
			if(min>99)
			{
				DisplayNum2(0,min/100);
				DisplayNum2(1,min/10%10);
			}
			else
			{
				DisplayNum2(0,min/10);
				DisplayNum2(1,min%10);
			}
		}
		else
		{
		DisplayNum2(1,min%10);
		}
    }
	  
	Y_Display(9);//显示时间图标
	
	if(sw!=0)
	{
		if(sw==9)
		{
		  for(i=1;i<9;i++){Y_Display(i);}//显示时间外框
		}
		else
		{
			if(sw>8){sw=8;}
			Y_Display(sw);
		}
    }
}

/***********************************************************************************************************************
-----函数功能	时间滚动
-----传入参数   tim 0常亮时间框--注意Display_Tim, 其他延时时间框格数时间
-----作者       LJD
-----日期       2026-01-10
************************************************************************************************************************/
void Display_TimRoll(u8 tim)//时间滚动
{
	static u8 i=1;
	static u8 tim_dly=0;
    u8 bat_grade=2;
	u8 t=0;
	
	if(tim==0)
	{
	  for(t=1;t<9;t++){Y_Display(t);}//显示时间外框
	}
	else
	{
	   bat_grade=i+1;	
		
		
			
	   for(;bat_grade<9;)
	   {
		  Y_Display(bat_grade);
		  bat_grade++;
		  t++;
	   }
	   
	   bat_grade=1;
	   t=8-t;
	   for(;bat_grade<t;)
	   {
		  Y_Display(bat_grade);
		  bat_grade++;
	   }
	   
		tim_dly++;
		if(tim_dly>=tim)
		{
			tim_dly=0;	   
		   i++;if(i>8)i=1;
		}
    }
}

