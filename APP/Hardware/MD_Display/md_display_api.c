#define CHG_BAT_DLY_TIM_S 2	//*300mS
/***********************************************************************************************************************
-----函数功能
-----传入参数   cds 1:充电,0放电
-----传入参数   en_sw 1闪烁，0不闪烁
-----传入参数   soc
-----作者       LJD
-----日期       2026-01-10
************************************************************************************************************************/
void Display_BAT(u8 cds,bool en_sw,u8 soc)
{
	u8 i=0;
	int bat_grade=0;
	static bool sw=false;
	static u8 chg_dey_tim=0;
	/*
    ===========BAT等级界面============
    【1】  充电统一闪烁--->CHG_BAT_DLY_TIM_S
    【2】  放电--->
	       [1]en_sw：1，低于<10%闪烁
	       [2]en_sw：0，低于<10%不闪烁
    */
	
	if(cds)
    {		
	   chg_dey_tim++;  
	   if(chg_dey_tim<CHG_BAT_DLY_TIM_S)
	   {			   
		   sw=false;
	   }
	   else if(chg_dey_tim<(CHG_BAT_DLY_TIM_S*2-1))
	   {
		  sw=true;
	   }
	   else if(chg_dey_tim<(CHG_BAT_DLY_TIM_S*2+1))
	   {
		  chg_dey_tim=0;
	   }
    }
   
    if(soc>=98)
    {
	   for(i=1;i<12;i++){T_Display(i);}     
    }
    else if(soc>=10)
    {
	   bat_grade=(11-(soc/10));
	
	   if(sw){bat_grade-=1;}
		  
	   
	   
	   for(;bat_grade<11;)
	   {
		   
          T_Display(bat_grade);
		  bat_grade++;
	   }
    }
	else if((soc>0)&&(soc<=9))
	{
		if(cds)
		{
		  if(sw)T_Display(10);
		}
		else
		{
			if(en_sw)
			{
			   if(sw)
			   {
				   sw=false;
				   T_Display(10);
			   }
			   else
			   {
				  sw=true;
			   }
			}
	    }
	}

    T_Display(11);//[]
	S_Display(30);//()
}

